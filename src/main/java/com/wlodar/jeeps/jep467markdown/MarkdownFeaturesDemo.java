package com.wlodar.jeeps.jep467markdown;

/// # Markdown Demo: Utilities
/// This class demonstrates **Markdown-style JavaDoc**
/// for use in JEP 467.
///
/// ## Features
/// - Easy to read
/// - Supports markdown headers
/// - Bullet and numbered lists
public class MarkdownFeaturesDemo {

    /// ## Summary
    /// Returns the square of an integer.
    ///
    /// ### Example
    /// ```
    /// int result = square(5); // 25
    /// ```
    public static int square(int x) {
        return x * x;
    }
}

